# A tuple of integers
T = (1, 2, 3)

# A tuple of strings
T = ('red', 'green', 'blue')

# A tuple with mixed datatypes
T = (1, 'abc', 1.23, True)

# An empty tuple
T = ()

# A tuple without parentheses
T = 1, 'abc', 1.23, True

T = (4,)
print(type(T))
# Prints <type 'tuple'>

# Not a tuple
T = (4)
print(type(T))
# Prints <type 'int'>
