# Short Hand If - single statement
x, y = 7, 5
if x > y: print('x is greater')

# Prints x is greater

# Short Hand If - multiple statements
x, y = 7, 5
if x > y: print('x is greater'); print('y is smaller'); print('x and y are not equal')

# Prints x is greater
# Prints y is smaller
# Prints x and y are not equal
