L = ['red', 'green', 'yellow']
L.extend([1, 2, 3])
print(L)
# Prints ['red', 'green', 'yellow', 1, 2, 3]

# concatenation operator
L = ['red', 'green', 'blue']
L = L + [1, 2, 3]
print(L)
# Prints ['red', 'green', 'blue', 1, 2, 3]

# augmented assignment operator
L = ['red', 'green', 'blue']
L += [1, 2, 3]
print(L)
# Prints ['red', 'green', 'blue', 1, 2, 3]
