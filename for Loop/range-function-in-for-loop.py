# Generate a sequence of numbers from 0 6
for x in range(7):
    print(x)
# Prints 0 1 2 3 4 5 6

# Print 'Hello!' three times
for x in range(3):
    print('Hello!')
# Prints Hello!
# Prints Hello!
# Prints Hello!


# Generate a sequence of numbers from 2 to 6
for x in range(2, 7):
    print(x)
# Prints 2 3 4 5 6


for x in range(-5,0):
    print(x)
# Prints -5 -4 -3 -2 -1


# Increment the range with 2
for x in range(2, 7, 2):
    print(x)
# Prints 2 4 6
